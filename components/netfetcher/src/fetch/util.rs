//! Small shared helpers for the fetch phases.

/// Case-insensitive header lookup over a `(name, value)` list.
pub(super) fn header_val<'a>(headers: &'a [(String, String)], name: &str) -> Option<&'a str> {
    headers
        .iter()
        .find(|(k, _)| k.eq_ignore_ascii_case(name))
        .map(|(_, v)| v.as_str())
}
